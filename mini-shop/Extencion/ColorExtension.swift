import UIKit

extension UIColor{
    class func buttonColor() -> UIColor{
        return UIColor(red: 113, green: 212, blue: 218, alpha: 1.0)
    }
    
    class func backgroundColor() -> UIColor{
        return UIColor(red: 255, green: 181, blue: 227, alpha: 1.0)
    }
}
